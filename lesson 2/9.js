// Отсортируйте массив по убыванию.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
 const arr = [1, 2, 3, 4, 5, 6];
// [6,5,4,3,2,1]

////////////////// Решение //////////////////
let countArr = 0;
for (countArr in arr) {}

for (let i = 1; i <= countArr; i++) {
    let k = i;

  while ( k>0 && (arr[k-1] < arr[k]) ) {
   let el = arr[k];
   arr[k] = arr[k-1]
    arr[k-1] = el;
     k = k - 1;
  }
}



